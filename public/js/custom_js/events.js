"use strict";
$(document).ready(function() {
    $('#datetimepicker6').datetimepicker({
        keepOpen: false,
        useCurrent: false,
        minDate: new Date().setHours(0, 0, 0, 0)
    });
    var date = new Date();
    date.setDate(date.getDate() - 1);
    $('#datetimepicker6').datetimepicker({
        startDate: date
    });
    $('#datetimepicker7').datetimepicker({
        keepOpen: false,
        useCurrent: false,
        minDate: new Date().setHours(0, 0, 0, 0)
    });
    $('#datetimepicker6').on("dp.change", function(e) {
        $('#datetimepicker7').data("DateTimePicker").minDate(e.date);
        $('#events_form').bootstrapValidator('revalidateField', 'date_start');
    });
    $('#datetimepicker7').on("dp.change", function(e) {
        $('#datetimepicker6').data("DateTimePicker").maxDate(e.date);
        $('#events_form').bootstrapValidator('revalidateField', 'date_end');
    });

    function validateEditor() {
        // Revalidate the content when its value is changed by Summernote
        $('#events_form').bootstrapValidator('revalidateField', 'content');
    };
    $('#events_form')
        .bootstrapValidator({
            excluded: [':disabled'],
            fields: {
                title: {
                    validators: {
                        notEmpty: {
                            message: 'The event title is required and cannot be empty'
                        }
                    }
                },
                date_start: {
                    validators: {
                        notEmpty: {
                            message: 'The start date is required and cannot be empty'
                        }
                    }
                },
                date_end: {
                    validators: {
                        notEmpty: {
                            message: 'The end date is required and cannot be empty'
                        }
                    }
                },
                content: {
                    validators: {
                        callback: {
                            message: 'The content is required and cannot be empty',
                            callback: function(value, validator) {
                                var code = $('[name="content"]').code();
                                // <p><br></p> is code generated by Summernote for empty content
                                return (code !== '' && code !== '<p><br></p>');
                            }
                        }
                    }
                }
            }
        }).on('summernote.change', function() {
            validateEditor();
            //$('#add_faq').bootstrapValidator('revalidateField', 'content');
        }).on('success.form.bv', function(e) {
            e.preventDefault();
            swal({
                title: "Success.",
                text: "Successfully Submitted",
                type: "success",
                allowOutsideClick: false
            }).then(function() {
                window.location.href = "events_list.html";
            });
        })
        .find('[name="content"]').summernote({
            height: 200
        });
    $('input[type=reset]').on('click', function() {
        $(".note-editable").empty();
        $('#events_form').bootstrapValidator("resetForm");
    });
});
